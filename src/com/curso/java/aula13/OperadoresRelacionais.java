package com.curso.java.aula13;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		
		int valor1 = 2;
		int valor2 = 3;
		
		System.out.println("O valor1 === valor2: " + (valor1 == valor2));
		System.out.println("O valor1 != valor2: " + (valor1 != valor2));
		System.out.println("O valor1 >= valor2: " + (valor1 >= valor2));
		System.out.println("O valor1 <= valor2: " + (valor1 <= valor2));
		System.out.println("O valor1 > valor2: " + (valor1 > valor2));
	}
	
}
